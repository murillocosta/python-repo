"""
bool (boolean)

True ou False


podem ser utilizados operadores lógicos
para verificações falsas e verdadeiras

==

"""

print('True: ', type(True))
print('False: ', type(False))
print('10 == 10: ', 10 == 10)
print('10 == 11: ', 10 == 11)
print('10 == 10.0: ', 10 == 10.0)
print('type(10) == type(10.0): ', type(10) == type(10.0))
print('10 == "10": ', 10 == '10')
